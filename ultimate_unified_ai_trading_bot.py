    def _get_model_prediction(self, model, features: np.ndarray, model_name: str, symbol: str) -> Dict:
        """Get prediction from a specific model"""
        
        # For untrained models, return demo prediction
        if not hasattr(model, 'predict'):
            return self._generate_demo_prediction(model_name, symbol)
        
        try:
            # Scale features if scaler exists
            if model_name in self.scalers and hasattr(self.scalers[model_name], 'transform'):
                features_scaled = self.scalers[model_name].transform(features)
            else:
                features_scaled = features
            
            # Get prediction
            prediction = model.predict(features_scaled)[0]
            probability = model.predict_proba(features_scaled)[0].max() if hasattr(model, 'predict_proba') else 0.7
            
            # Convert to action
            actions = ['SELL', 'HOLD', 'BUY']
            action = actions[prediction] if prediction < len(actions) else 'HOLD'
            
            return {
                'action': action,
                'confidence': float(probability),
                'probability': float(probability)
            }
            
        except Exception as e:
            logger.warning(f"⚠️ Model {model_name} prediction error: {e}")
            return self._generate_demo_prediction(model_name, symbol)
    
