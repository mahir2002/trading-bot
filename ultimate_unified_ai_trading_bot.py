    def _create_comprehensive_signal(self, symbol: str, market_data: pd.DataFrame, 
                                   technical_data: pd.DataFrame, sentiment_data: Dict, 
                                   ai_predictions: Dict) -> TradingSignal:
        """Create comprehensive trading signal combining ALL data"""
        
        latest_data = technical_data.iloc[-1]
        current_price = float(latest_data['close'])
        
        # Ensemble AI prediction
        actions = ['SELL', 'HOLD', 'BUY']
        action_scores = {action: 0 for action in actions}
        total_confidence = 0
        
        for model_name, prediction in ai_predictions.items():
            weight = self.model_weights.get(model_name, 0.2)
            action_scores[prediction['action']] += weight * prediction['confidence']
            total_confidence += weight * prediction['confidence']
        
        # Get final action
        final_action = max(action_scores.keys(), key=lambda k: action_scores[k])
        final_confidence = action_scores[final_action] / max(sum(self.model_weights.values()), 1)
        
        # Calculate risk metrics
        volatility = float(latest_data.get('volatility', 0.02))
        stop_loss_price = current_price * (1 - self.config.stop_loss) if final_action == 'BUY' else current_price * (1 + self.config.stop_loss)
        take_profit_price = current_price * (1 + self.config.take_profit) if final_action == 'BUY' else current_price * (1 - self.config.take_profit)
        
        # Create comprehensive signal
        signal = TradingSignal(
            symbol=symbol,
            action=final_action,
            confidence=final_confidence,
            price=current_price,
            predicted_price=current_price * (1.02 if final_action == 'BUY' else 0.98 if final_action == 'SELL' else 1.0),
            
            # AI Model Predictions
            random_forest_signal=ai_predictions.get('random_forest', {}).get('action', 'HOLD'),
            lstm_signal=ai_predictions.get('lstm', {}).get('action', 'HOLD'),
            transformer_signal=ai_predictions.get('transformer', {}).get('action', 'HOLD'),
            xgboost_signal=ai_predictions.get('xgboost', {}).get('action', 'HOLD'),
            lightgbm_signal=ai_predictions.get('lightgbm', {}).get('action', 'HOLD'),
            ensemble_signal=final_action,
            
            # Technical Analysis
            rsi=float(latest_data.get('rsi_14', 50)),
            macd=float(latest_data.get('macd', 0)),
            bollinger_position=float(latest_data.get('bb_position', 0.5)),
            sma_trend='BULLISH' if latest_data.get('close', 0) > latest_data.get('sma_20', 0) else 'BEARISH',
            volume_trend='HIGH' if latest_data.get('volume_ratio', 1) > 1.2 else 'NORMAL',
            
            # Sentiment Analysis
            twitter_sentiment=sentiment_data.get('twitter_sentiment', 0),
            reddit_sentiment=sentiment_data.get('reddit_sentiment', 0),
            news_sentiment=sentiment_data.get('news_sentiment', 0),
            overall_sentiment=sentiment_data.get('overall_sentiment', 0),
            
            # Risk Metrics
            volatility=volatility,
            stop_loss_price=stop_loss_price,
            take_profit_price=take_profit_price,
            risk_reward_ratio=abs(take_profit_price - current_price) / abs(stop_loss_price - current_price),
            
            # Market Data
            timestamp=datetime.now(),
            exchange='binance' if 'binance' in self.exchanges else 'demo',
            volume_24h=float(latest_data.get('volume', 0)),
            market_cap=0.0  # Would be fetched from API in production
        )
        
        return signal

    def _apply_risk_management(self, signal: TradingSignal) -> TradingSignal:
        """Apply comprehensive risk management"""
        
        # Check confidence threshold
        if signal.confidence < self.config.confidence_threshold:
            signal.action = 'HOLD'
            signal.confidence = 0.5
        
        # Check position limits
        if len(self.portfolio['positions']) >= self.config.max_positions:
            if signal.action in ['BUY', 'STRONG_BUY']:
                signal.action = 'HOLD'
        
        # Check daily loss limits
        if self.portfolio['daily_pnl'] < -self.config.max_daily_loss * self.portfolio['total_value']:
            signal.action = 'HOLD'
        
        return signal

