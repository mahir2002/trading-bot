        df['price_change'] = df['close'].pct_change()
        df['volatility'] = df['price_change'].rolling(window=20).std()
        df['volatility_rank'] = df['volatility'].rolling(window=252).rank(pct=True)
        
        # Trend strength
        df['trend_strength'] = (df['close'] - df['sma_50']) / df['sma_50']
        
        # Price position relative to range
        df['price_position'] = (df['close'] - df['low'].rolling(20).min()) / (df['high'].rolling(20).max() - df['low'].rolling(20).min())
        
