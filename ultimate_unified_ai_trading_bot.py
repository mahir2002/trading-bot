    def _generate_demo_data(self, symbol: str) -> pd.DataFrame:
        """Generate demo market data for testing"""
        periods = 1000
        dates = pd.date_range(end=datetime.now(), periods=periods, freq='H')
        
        # Base price from symbol hash
        base_price = 100 + (hash(symbol) % 50000)
        
        # Generate realistic price movements
        np.random.seed(hash(symbol) % 2**32)
        returns = np.random.normal(0.0001, 0.02, periods)
        prices = [base_price]
        
        for ret in returns[1:]:
            new_price = prices[-1] * (1 + ret)
            prices.append(max(new_price, 0.01))
        
        df = pd.DataFrame({
            'timestamp': dates,
            'close': prices
        })
        
        df['open'] = df['close'].shift(1).fillna(df['close'].iloc[0])
        df['high'] = df[['open', 'close']].max(axis=1) * (1 + np.random.uniform(0, 0.01, len(df)))
        df['low'] = df[['open', 'close']].min(axis=1) * (1 - np.random.uniform(0, 0.01, len(df)))
        df['volume'] = np.random.lognormal(10, 1, len(df))
        
        return df

